<br>
Важная особенность —&nbsp;возможность хранить и экспортировать данные в другие сервисы, среди которых: Salesforce, Google BigQuery, Amplitude и Tableau.<br>
<br>
<h3>Дополнительная функциональность</h3><br>
<img src="https://habrastorage.org/getpro/habr/post_images/f60/fb5/418/f60fb5418b9b99b9333061030b0b322c.png"><br>
