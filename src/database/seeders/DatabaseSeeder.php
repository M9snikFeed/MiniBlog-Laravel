Ко всем инструментам описана подробная документация, также существует несколько туториалов и готовых примеров использования платформы.<br>
<br>
<h3>Надежность</h3><br>
К сожалению, мне не удалось найти никакой статистики по стабильности серверов работы сервиса Kumulos.<br>
<br>
<h3>Стоимость</h3><br>
Помимо бесплатного триала у Kumulos есть 3 <a href="https://www.kumulos.com/pricing/">платных плана</a>: Startup, Enterprise и Agency. Они работают по принципу «плачу только за то, что использую». К сожалению, сервис не предоставляет прайс-лист в открытом доступе, похоже, что он рассчитывается индивидуально, исходя из ваших потребностей.<br>
