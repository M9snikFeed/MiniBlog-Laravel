<h3>Стоимость</h3><br>
В <a href="https://azure.microsoft.com/ru-ru/pricing/details/app-service/windows/">ценовой политике</a> Microsoft Azure разные тарифы оплаты сервиса, есть и бесплатный план с определенными лимитами, которых хватит для тестирования. Важно помнить, что Azure — IaaS сервис, большинство которых из-за своей специфики и сложности подсчета отработанных ресурсов, страдают от сложности&nbsp;прогнозирования стоимости работы. Многие сталкиваются с трудностями и часто даже невозможностью правильно посчитать используемые мощности. Реальный счет может значительно отличаться от того, на который рассчитывали.&nbsp;<br>
