<h2>Заключение</h2><br>
К вопросу выбора облачного сервиса для мобильного бэкенда важно подходить со всей серьезностью, так как он окажет драматическое влияние на процесс <a href="https://surf.ru/flutter/">разработки вашего приложения</a> или сервиса и его последующего развития.&nbsp;<br>
<br>
В статье мы рассмотрели&nbsp;4 сервиса: Microsoft Azure, AWS Amplify, Google Firebase и Kumulos. Среди них 2&nbsp; крупных IaaS сервиса и 2 MBaaS, которые специализируются именно на мобильном бэкэнде. И в каждом из вариантов встретили определенные проблемы и негативные стороны.<br>
<br>
Важно понимать —&nbsp;идеального решения нет.&nbsp; Выбор технологии для проекта —&nbsp;компромисс между ключевыми факторами. Предлагаю еще раз по ним пройтись:<br>
<br>
<h4>Функциональность</h4><br>
От функциональности платформы, которую вы выберете, напрямую зависят и ограничения, которые накладываете на ваш бэкэнд. Всегда нужно четко осознавать, какие у вас приоритеты при выборе сервиса, будь то использование одной конкретной фичи, например, push-уведомлений в целях экономии, либо постройка собственной инфраструктуры в рамках одной экосистемы для централизации и единообразия вашего бэкенда.&nbsp;<br>
<br>
<h4>Аналитика</h4><br>
Тяжело представить современные сервисы без аналитики. Ведь именно этот инструмент позволяет улучшать сервис, анализировать пользователей и в итоге получать большую прибыль. От качества и функциональности аналитики напрямую зависит качество финального продукта. Но никто не мешает подключать и стороннюю аналитику, будь это аналитическая часть Firebase, AppMetrica от Яндекса или что-то другое, более подходящее для вас.<br>
<br>
<h4>Сложность интеграции</h4><br>
Сложность интеграции напрямую влияет на затраты как денежных, так и временных ресурсов в процессе разработки, не говоря уже о возможном усложнении процесса поиска разработчиков из-за непопулярности, либо высокого порога вхождения в инструментарий.<br>
<br>
<h4>Надежность и стабильность</h4><br>
Надежность и стабильность любого сервиса —&nbsp;одни из важнейших показателей. А когда собственное приложение страдает от проблем на стороне провайдера, получается ситуация не из приятных. Конечному пользователю все равно что там не так и виноваты ли конкретно вы в том, что сервис не работает. Он не сможет сделать, что планировал, и все, впечатление испорчено, к продукту он возможно уже не вернется. Да, идеальных сервисов не бывает, но бывают инструменты для минимизации потерь при проблемах на стороне провайдера.<br>
<br>
<h4>Ценовая политика</h4><br>
Ценовая политика сервиса для многих определяющий фактор, так как если финансовые возможности не сходятся с запросами провайдера, то вы просто не сможете продолжать совместную работу. Важно учитывать и прогнозировать стоимость сервисов от которых зависит ваш продукт. Ценообразование у всех сервисов разное, но чаще всего оно пропорционально ресурсам, которые вы используете, будь это количество отправленных уведомлений или используемый размер жесткого диска хранилища.<br>
