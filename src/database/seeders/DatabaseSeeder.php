<h2>Microsoft Azure</h2><br>
<br>
<img src="https://habrastorage.org/webt/zj/ct/zv/zjctzvfwpbw_eyugjgdig4-tohi.png"><br>
<br>
<a href="https://azure.microsoft.com/ru-ru/">Microsoft Azure</a> —&nbsp;Infrastructure-As-A-Service (IaaS) сервис, который содержит в себе полноценную BaaS функциональность и помогает при создании бэкенда для мобильных приложений.<br>
<br>
<h3>MBaaS</h3><br>
<a href="https://azure.microsoft.com/ru-ru/free/services/mobile-apps/">Microsoft Azure</a> располагает полным набором функциональности для создания бэкенда для мобильного приложения. Обработка push-уведомлений, автоматическое масштабирование, синхронизация данных, интеграция с социальными сетями и многое другое.<br>
<br>
Важная особенность Azure —&nbsp;географическое положение серверов. Они расположены в 54 регионах мира, что повышает вероятность подобрать для себя подходящий по задержке сервер. Поскольку в случае неполадок чаще всего страдают только отдельные регионы, можно предположить, что чем больше регионов, тем меньше вероятность попасть на тот самый «нестабильный». Как утверждают Microsoft, у них больше регионов, чем у любого другого поставщика облачных решений. Это, несомненно, плюс.<br>
<br>
<img src="https://habrastorage.org/getpro/habr/post_images/85b/d3b/863/85bd3b863f1271cbcda091e40c8f6895.png"><br>
<br>
<h3>Аналитика</h3><br>
Сервис предоставляет возможность в реальном времени мониторить работоспособность приложений и собирать отчёты о «падениях». Позволяя тем самым мгновенно локализовать и решить проблему.<br>
<br>
Также в Azure можно использовать их собственную библиотеку для сбора аналитики в приложениях: собирать основные метрики (информация об устройстве, о сессии, об активности пользователей и многое другое) и создавать собственные события для отслеживания. Все собранные данные сразу экспортируются в Azure, позволяя проводить с ними аналитическую работу в удобном формате<br>
<br>
<h3>Дополнительная функциональность</h3><br>
Также существуют интересные функции типа тестирования сборок приложений на реальных устройствах, настройки CI/CD для автоматизации процесса разработки и инструментарий для отправки сборок приложений на бета-тестирование или сразу в App Store или Google Play<br>
<br>
Azure позволяет использовать фреймворк «из коробки» предназначенный для работы с картами и геопространственными данными, что упрощает&nbsp;работу с подобным форматом.<br>
<br>
Особенно интересно выглядит возможность решения задач с использованием <a href="https://azure.microsoft.com/ru-ru/overview/ai-platform/">искусственного интеллекта</a>, при помощи которого можно прогнозировать различные аналитические показатели и использовать готовые к работе инструменты для компьютерного зрения, распознавания речи и много другого.<br>
<br>
<h3>Сложность интеграции</h3><br>
Сервис Microsoft Azure предоставляет <a href="https://azure.microsoft.com/ru-ru/tools/">SDK</a> для основных мобильных платформ (iOS и Android) и, что бывает не часто, для кроссплатформенных решений (Xamarin и PhoneGap).&nbsp;<br>
<br>
В целом, пользователи жалуются на сложный интерфейс и высокий порог вхождения. Это говорит о возможных проблемах в интеграции сервиса.&nbsp;<br>
<br>
Важно понимать, что высокий порог вхождения — не частный случай с Azure, а общая проблема для IaaS. Например, Amazon Web Services, который будет рассмотрен далее, также подвержен данному недугу еще больше.<br>
<br>
<h3>Надежность</h3><br>
