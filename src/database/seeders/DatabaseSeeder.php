<h4>Vendor lock</h4><br>
Используя эти сервисы важно не завязываться на одном решении, иначе&nbsp;вы становитесь полностью зависимыми от него и обрекаете себя, на так называемый, «vendor lock». Это значит, что если с сервисом что-то случится, изменится владелец, направление развития или закроется — придется в срочном порядке искать нового MBaaS поставщика, и, в зависимости от размеров приложения, подобный переезд потребует существенных временных, и, как следствие, денежных затрат. Особенно страшно будет, если бэкэнд завязан на какой-либо уникальной функциональности MBaaS-провайдера, так как все поставщики разные и далеко не у всех одинаковый набор функционала. Поэтому редко, когда удается переехать «безболезненно».<br>
