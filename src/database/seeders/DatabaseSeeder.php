<h2>Google Firebase</h2><br>
<img src="https://habrastorage.org/webt/xd/xq/sy/xdxqsys5pkzex2nxnkjyjslw0wi.png"><br>
Сервис <a href="https://firebase.google.com/">Firebase</a> от Google является одним из самых интересных вариантов в качестве MBaaS сервиса для вашего приложения. Он давно зарекомендовал себя в качестве полезного инструмента и является таковым для многих известных приложений: Shazam, Duolingo, Lyft и других.&nbsp;<br>
<img src="https://habrastorage.org/getpro/habr/post_images/910/567/b2c/910567b2c1fc6651a045b0d170499f76.png"><br>
<br>
<h3>MBaaS</h3><br>
<img src="https://habrastorage.org/getpro/habr/post_images/ca2/f03/05e/ca2f0305e8a6c7d09f74e3212cfa9756.png"><br>
<br>
Firebase берет на себя все, что понадобится вашему мобильному приложению. Сервис совмещает в себе полноценные бэкенд-фичи, такие как хранение данных, синхронизация, аутентификация, облачные функции (выполнение бэкенд кода), и, в данный момент, в бете находится <a href="https://firebase.google.com/docs/ml-kit">Machine Learning Kit</a>, при помощи которого реализуется&nbsp; в приложении различная функциональность на основе машинного обучения (распознавание текста, объектов на фотографиях и много другое).&nbsp;<br>
<br>
<h3>Аналитика</h3><br>
Важная особенность Firebase в том, что помимо бэкенд функциональности, сервис предлагает и широкий спектр возможностей для аналитики приложения. Встроенная Google Analytics, сегментирование пользовательской базы и работа с push-уведомлениями. Также в 2017 году Google отметился крутым приобретением, купив широко распространенный сервис Fabric и интегрировав его в Firebase наряду с Crashlytics, крайне полезным инструментом для отслеживания ошибок в приложении и сбора статистики и отчетов о падениях, произошедших на устройствах пользователей.<br>
<br>
<h3>Дополнительная функциональность</h3><br>
Firebase предоставляет инструмент <a href="https://firebase.google.com/docs/dynamic-links">Firebase Dynamic Links</a> для обработки динамических ссылок на ваш контент, при помощи этого инструмента можно генерировать ссылки, которые ведут в приложение, если оно установлено, если нет — отправляют пользователя в App Store или Google Play для установки. Также подобные ссылки работают в зависимости от устройства, на котором они открываются, если это компьютер, то будет открыта страница в браузере, а если устройство — произойдет переход в приложение.<br>
<br>
Также Google позволяет проводить A/B тестирование ваших приложений при помощи <a href="https://firebase.google.com/docs/ab-testing">Firebase A/B Testing</a> и настраивать удаленную конфигурацию с инструментом <a href="https://firebase.google.com/docs/remote-config/?gclid=Cj0KCQjws7TqBRDgARIsAAHLHP4O7dQKcwyz7BgHxoaNMyg4_WtMvDdJRlKy7FReH26kTYJvK1YzX6gaAtLkEALw_wcB">Remote Config</a>.&nbsp;<br>
<br>
<h3>Сложность интеграции</h3><br>
Становится понятно, что этот сервис совмещает в себе крайне большое количество возможностей для вашего приложения. Для интеграции Firebase стоит использовать <a href="https://firebase.google.com/docs?hl=RU">SDK</a> необходимой платформы, среди которых iOS, Android, JavaScript, а также для C++ и Unity, что будет очень кстати, если вы разрабатываете игры. Важно отметить, что у Firebase достаточно подробная документация и широкая база пользователей-разработчиков, и как следствие, большое количество&nbsp; вспомогательного контента в сети, будь то ответы на вопросы или обзорные статьи.<br>
<br>
<h3>Надежность</h3><br>
Стоит ли полагаться на Google —&nbsp;вопрос отдельной статьи. С одной стороны, у вас есть высокостабильный и работающий провайдер, а с другой, никогда не знаешь, когда «Гугл закроет и этот сервис». Не зря Гугл убрали у себя из миссии <i>«Dont be evil»</i>.&nbsp;<br>
<br>
<img src="https://habrastorage.org/getpro/habr/post_images/f20/80c/10f/f2080c10f7b49435666375e5ffd57fc7.png"><br>
<br>
Когда провайдер обладает такими ресурсами, казалось бы, аптайм должен стремиться к 100%, но все равно можно найти множество сообщений о проблемах с сервисом, например, <a href="https://medium.com/@scosta/why-firebase-sucks-ce5d2302eb20">цитата</a> одного из пользователей: <i>«Downtime happens. In the case of Firebase, you might say that «uptime» happens»</i>. И действительно, если посмотреть на статистику по событиям с сервисами Firebase, увидим, что бывают как небольшие простои, так и полноценные отключения на 5-7 часов, это может быть критично для вашего сервиса.<br>
<br>
<img src="https://habrastorage.org/getpro/habr/post_images/123/fc6/36f/123fc636fb106885971b778b56addf33.png"><br>
<br>
А бывает, что проблемы длятся неделями. Нельзя забывать, что на этих сервисах может работать критичный и жизненно важный для продукта код. Выглядит данная статистика не очень радостно.<br>
<br>
<h3>Стоимость</h3><br>
<img src="https://habrastorage.org/getpro/habr/post_images/60f/7fb/4f8/60f7fb4f8b2cc6ae0fb31d917751e3c0.png"><br>
<br>
<a href="https://firebase.google.com/pricing">Ценовая политика</a> Firebase понятна и проста, есть 3 плана: Spark, Flame и Blaze. Они идеологически отличаются друг от друга. В то время как Spark —&nbsp;бесплатный план с лимитами, которые позволяют развернуть и протестировать значительную часть функциональности платформы. Планы Flame и Blaze предполагают платное использование. Flame стоит фиксированные 25$ в месяц, но по сути вы получаете тот же Spark, только со значительно большими лимитами.&nbsp;<br>
<br>
Blaze отличается от остальных. Он позволяет использовать возможности платформы в неограниченных количествах, при этом вы платите соразмерно ресурсам, которые используете. Это крайне гибкий план, в котором оплачиваете только ту функциональность, которой пользуетесь. Если, например, решите использовать платформу только для тестирования приложений — только за превышение бесплатных лимитов по тестированию и будете платить.<br>
<br>
В общем, ценообразование Firebase весьма прозрачно и прогнозируемо. В процессе понимаешь, сколько будет стоить та или иная функциональность, а также рассчитываешь стоимость при масштабировании или изменениях в сервисе.<br>
<br>
<h3>Итог по Firebase</h3><br>
Сервис Firebase от Google представляет собой полноценный MBaaS провайдер, который ограничивает от инфраструктурных сложностей, с которыми напрямую связаны AWS и Azure. Весь необходимый для разработки облачного бэкенда функционал на месте, широкие возможности для аналитики, относительная простота интеграции, довольно низкий порог вхождения и прозрачное ценообразование.&nbsp;<br>
