Мобильное решение от Amazon позволяет в короткие сроки сконфигурировать всю необходимую функциональность для мобильного приложения. Будь то серверная логика, хранение данных, авторизация пользователей или обработка и доставка контента, уведомления и аналитика.&nbsp;<br>
<br>
Amazon также предоставляет все необходимые условия с точки зрения инфраструктуры, такие как масштабирование, балансировка нагрузки и многое другое.<br>
<br>
<h3>Аналитика</h3><br>
За аналитику отвечает отдельный сервис <a href="https://aws.amazon.com/ru/pinpoint/">Amazon Pinpoint</a>, в котором можно сегментировать аудиторию и проводить масштабные таргетинговые кампании через разные каналы (push уведомления, смс и электронная почта) по привлечению пользователей в сервис.<br>
<br>
Pinpoint предоставляет данные в режиме реального времени, можно создавать динамические сегменты аудитории, анализировать их вовлеченность и на основе этих данных оптимизировать маркетинговую стратегию.<br>
<br>
<h3>Дополнительная функциональность</h3><br>
Amazon Amplify предоставляет доступ к сервису <a href="https://aws.amazon.com/ru/device-farm/">AWS Device Farm</a> для тестирования билдов ваших приложений на реальных устройствах. Сервис позволяет проводить параллельное автоматизированное тестирование ваших приложений на множестве физических устройств, также доступно и ручное тестирование.<br>
<br>
Сервис <a href="https://aws.amazon.com/ru/amplify/console/">AWS Amplify Console</a> является инструментом для деплоя и хостинга как серверных ресурсов, так и веб приложений с возможностью настройки CI/CD для автоматизации процесса разработки.<br>
<br>
Также необычно выглядит возможность внедрения в мобильные приложения «из коробки» голосовых и текстовых ботов в качестве интерфейса для взаимодействия с пользователем. Работает это на сервисе <a href="https://aws.amazon.com/ru/lex/">Amazon Lex</a>.<br>
<br>
Интересно, что AWS Amplify предоставляет также и небольшую <a href="https://aws-amplify.github.io/media/ui_library">библиотеку</a> готовых UI компонентов для вашего React Native приложения, что может послужить незначительным ускорением процесса разработки, либо использоваться в прототипе или MVP вашего проекта.<br>
