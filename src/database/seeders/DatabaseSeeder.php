<h3>Сложность интеграции</h3><br>
Сервис Amazon Amplify предоставляет SDK для <a href="https://aws-amplify.github.io/docs/ios/start">iOS</a>, <a href="https://aws-amplify.github.io/docs/android/start">Android</a>, <a href="https://aws-amplify.github.io/docs/js/start?platform=purejs">JavaScript</a> и <a href="https://aws-amplify.github.io/docs/js/start?platform=react-native">React Native</a> и достаточно подробную <a href="https://aws-amplify.github.io/docs/">документацию</a>. Важно отметить что помимо REST, сервис поддерживает еще и GraphQL.<br>
<br>
Как говорилось в процессе анализа Azure, высокий порог вхождения —общая проблема для всех IaaS. Amazon не исключение, а даже наоборот. Это, наверное, один из самых сложных сервисов для понимания. Это происходит из-за большого количества различных инструментов, которыми располагает AWS. Освоение AWS с нуля займет значительное время. Но если ограничиться только Amplify — можно реализовать рабочее решение в адекватные сроки.<br>
