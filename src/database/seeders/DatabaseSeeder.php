<a href="https://aws.amazon.com/ru/">Amazon Web Services (AWS)</a> — второй IaaS, который попал в нашу подборку. Он представляет огромное количество сервисов и интересен тем, что у него по аналогии с Microsoft Azure существует выделенный набор функциональности под названием <a href="https://aws.amazon.com/ru/amplify/">AWS Amplify</a>, который по сути и является мобильным бэкэндом. Ранее вы могли слышать название AWS Mobile Hub, который долгое время являлся основным сервисом, предоставляющим MBaaS функциональность. Как <a href="https://aws.amazon.com/ru/blogs/mobile/aws-mobile-gets-amplified/">пишут</a> сами Amazon, Amplify это доработанный и усовершенствованный Mobile Hub, в котором решены основные проблемы предшественника.<br>
